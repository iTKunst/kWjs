define
(
	[	
		"../obj",
	], 
	function
	(
		h
	)
	{
			
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function h6()
		{
			//console.log("h6::constructor");
			this.m_sKWTag = "h6";
		}

		h6.prototype = new h();
		h6.prototype.constructor = h6;
		h6.constructor = h.prototype.constructor;

		h6.prototype.check = 
			function check()
		{
			h.prototype.check.call(this);
			//console.log(this.kWLogCalled());
		};

		h6.prototype.init = 
			function init()
		{
			//console.log(this.kWLogCalled());
			h.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***		public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public callbacks
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h6.prototype.h6InitOR =
			function h6InitOR()
		{
			console.error(this.kWLogNotImpl());
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (non-overrides)
	//***
	//*******************************************************************//

		h6.prototype.hInitOR =
			function()
		{
			this.h6Init();
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h6.prototype.h6Init = 
			function h6Init()
		{
			//console.log(this.kWLogCalled());
			this.h6InitOR();
		};

		return h6;

	}
);
