define
(
	[	
		"../obj",
	], 
	function
	(
		h
	)
	{
			
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function h4()
		{
			//console.log("h4::constructor");
			this.m_sKWTag = "h4";
		}

		h4.prototype = new h();
		h4.prototype.constructor = h4;
		h4.constructor = h.prototype.constructor;

		h4.prototype.check = 
			function check()
		{
			h.prototype.check.call(this);
			//console.log(this.kWLogCalled());
		};

		h4.prototype.init = 
			function init()
		{
			//console.log(this.kWLogCalled());
			h.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***		public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public callbacks
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h4.prototype.h4InitOR = 
			function h4InitOR()
		{
			console.error(this.kWLogNotImpl());
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (non-overrides)
	//***
	//*******************************************************************//

		h4.prototype.hInitOR = 
			function()
		{
			this.h4Init();
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h4.prototype.h4Init = 
			function h4Init()
		{
			//console.log(this.kWLogCalled());
			this.h4InitOR();
		};

		return h4;

	}
);
