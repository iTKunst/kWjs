define
(
	[	
		"../obj",
	], 
	function
	(
		h
	)
	{
			
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function h3()
		{
			//console.log("h3::constructor");
			this.m_sKWTag = "h3";
		}

		h3.prototype = new h();
		h3.prototype.constructor = h3;
		h3.constructor = h.prototype.constructor;

		h3.prototype.check = 
			function check()
		{
			h.prototype.check.call(this);
			//console.log(this.kWLogCalled());
		};

		h3.prototype.init = 
			function init()
		{
			//console.log(this.kWLogCalled());
			h.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***		public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public callbacks
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h3.prototype.h3InitOR = 
			function h3InitOR()
		{
			console.error(this.kWLogNotImpl());
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (non-overrides)
	//***
	//*******************************************************************//

		h3.prototype.hInitOR =
			function()
		{
			this.h3Init();
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h3.prototype.h3Init = 
			function h3Init()
		{
			//console.log(this.kWLogCalled());
			this.h3InitOR();
		};

		return h3;

	}
);
