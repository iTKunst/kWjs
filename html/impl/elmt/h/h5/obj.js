define
(
	[	
		"../obj",
	], 
	function
	(
		h
	)
	{
			
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function h5()
		{
			//console.log("h5::constructor");
			this.m_sKWTag = "h5";
		}

		h5.prototype = new h();
		h5.prototype.constructor = h5;
		h5.constructor = h.prototype.constructor;

		h5.prototype.check =
			function check()
		{
			h.prototype.check.call(this);
			//console.log(this.kWLogCalled());
		};

		h5.prototype.init = 
			function init()
		{
			//console.log(this.kWLogCalled());
			h.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***		public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public callbacks
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h5.prototype.h5InitOR =
			function h5InitOR()
		{
			console.error(this.kWLogNotImpl());
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (non-overrides)
	//***
	//*******************************************************************//

		h5.prototype.hInitOR = 
			function()
		{
			this.h5Init();
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h5.prototype.h5Init = 
			function h5Init()
		{
			//console.log(this.kWLogCalled());
			this.h5InitOR();
		};

		return h5;

	}
);
