define
(
	[	
		"../obj",
		"../stat/enums"
	], 
	function
	(
		msg,
		enums
	)
	{
		
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function err() 
		{
			this.m_nKWMode	= enums.nErr;

			this.m_sKWMode	= "error";

			this.m_sKWText 	= "error";
		}

		err.prototype = new msg();
		err.prototype.constructor = err;
		err.constructor = msg.prototype.constructor;

		err.prototype.check = 
			function()
		{
			//this.bLogCheck();

			if (!this.m_sKWText)
			{
				this.bLogInvalid("m_sKWText");
			}

			msg.prototype.check.call(this);
		};

		err.prototype.init =
			function()
		{
			//this.bLogInit();
			msg.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***				public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***				public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***				protected
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***				protected (non-overridable)
	//***
	//*******************************************************************//

		err.prototype.msgCreateTextImpl =
			function()
		{
			this.errCreateText();
		};

	//*******************************************************************//
	//***																	   
	//***				protected (overridable)
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***				private
	//***
	//*******************************************************************//

		err.prototype.errCreateText = 
			function()
		{
			//this.bLogCalled("errCreateText");
		};

		return err;
		
	}
)