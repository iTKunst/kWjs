define
(
	[	
		"../obj"
	], 
	function
	(
		h
	)
	{
			
	//*******************************************************************//
	//***																	   
	//***		public initializors
	//***
	//*******************************************************************//

		function h2()
		{
			//console.log("h2::constructor");
			this.m_sKWTag = "h2";
		}

		h2.prototype = new h();
		h2.prototype.constructor = h2;
		h2.constructor = h.prototype.constructor;

		h2.prototype.check = 
			function check()
		{
			h.prototype.check.call(this);
			//console.log(this.kWLogCalled());
		};

		h2.prototype.init =
			function init()
		{
			//console.log(this.kWLogCalled());
			h.prototype.init.call(this);
		};

	//*******************************************************************//
	//***																	   
	//***		public accessors
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public callbacks
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		public methods
	//***
	//*******************************************************************//

	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h2.prototype.h2InitOR = 
			function h2InitOR()
		{
			console.error(this.kWLogNotImpl());
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (non-overrides)
	//***
	//*******************************************************************//

		h2.prototype.hInitOR = 
			function()
		{
			this.h2Init();
		};
		
	//*******************************************************************//
	//***																	   
	//***		private methods (overrides)
	//***
	//*******************************************************************//

		h2.prototype.h2Init = 
			function h2Init()
		{
			//console.log(this.kWLogCalled());
			this.h2InitOR();
		};

		return h2;

	}
);
